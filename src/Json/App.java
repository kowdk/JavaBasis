package Json;

public class App {
	String name;
	int age;
	String[] messages;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String[] getMessage() {
		return messages;
	}

	public void setMessage(String[] message) {
		this.messages = message;
	}

}
